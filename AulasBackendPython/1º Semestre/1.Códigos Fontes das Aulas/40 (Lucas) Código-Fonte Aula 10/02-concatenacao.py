# Concatenação de listas
lista1 = [1, 2, 3]
lista2 = [4, 5, 6]

# Concatenando lista 1 e lista 2
print(lista1 + lista2)

# Agora com uma nova lista
print([-2, -1, 0] + lista1)


# Concatenação de Strings
lang = "Python"
versao = "3"

# Concatenando lang e versao
print(lang + versao)

# Agora com um espaço a mais
print(lang + " " + versao)