'''
Programa que calcula e informa a soma de dois valores
ENTRADA: Dois valores inteiros
SAÍDA  : Soma dos valores informados
'''

num1 = int(input("Informe o primeiro valor: ")) # impressão de mensagem e leitura do primeiro valor
num2 = int(input("Informe o segundo valor: ")) # impressão de mensagem e leitura do segundo valor
    
soma = num1 + num2 # armazena o resultado na variável soma

print("Resultado da soma:", soma); # e mostra na tela

