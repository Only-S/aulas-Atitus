# Fatiamento de listas
notas = [8.5, 7, 6, 10, 9.1]

# Primeiras 3 notas
print(notas[:3])

# Posição 3 até o fim
print(notas[3:])

# Do início menos a última nota
print(notas[:-1])


# Fatiamento de strings
lang = "Python 3"

# Primeiras 6 letras
print(lang[:6])

# Posição 6 até o fim
print(lang[6:])

# Do início menos a última letra
print(lang[:-1])
